void sleep_ms(unsigned long milliseconds) {
    struct timespec ts;

    ts.tv_sec = milliseconds / 1000;
    ts.tv_nsec = (milliseconds % 1000) * 1000000;

    nanosleep(&ts, NULL);
}

void play(int anim[NB_IMAGES_ANIME_1_2][HEIGTH][WIDTH], int nb, int speed, int nbImages){
    printf("\e[?25l\e[2J");
    for (int l = 0; l < nb; l++)
    {
        for (int k = 0; k < nbImages; k++)
        {
            printf("\e[1;1H");
            for (int i = 0; i < HEIGTH; i++)
            {
                for (int j = 0; j < WIDTH; j++)
                {
                    printf("\e[4%dm ", anim[k][i][j]);
                    fflush(stdout);
                }
                printf("\e[40m\n");
            }
            sleep_ms(speed);
        }
    }
    printf("\e[?25h");
}

